import Foundation

import UIKit

extension UIColor {
    
    
    var teal:UIColor {
        
        get {
            
            return UIColor(red: 0, green: 181/255, blue: 218/255 , alpha: 1.0)

        }
    }
    
    
    var teal60: UIColor {
        
        get {
            return UIColor(red: 141/255, green: 203/255, blue: 218/255 , alpha: 1.0)
            }
    }
    
    var red: UIColor {
        
        get {
            
            return UIColor(red: 238/255, green: 46/255, blue: 36/255, alpha: 1.0)
            
        }
    }
    
    
    var orange: UIColor {
        
        get {
            
            return UIColor(red: 241/255, green: 92/255, blue: 34/255 , alpha: 1.0)
            
        }
    }
    
    var olive: UIColor {
        
        get {
            
            return UIColor(red: 215/255, green: 197/255, blue: 0 , alpha: 1.0)
            
        }
    }
    
    var grey: UIColor {
        
        get {
            
            return UIColor(red: 173/255, green: 175/255, blue: 178/255 , alpha: 1.0)
            
        }
    }
    
    var green: UIColor {
        
        get {
            
            return UIColor(red: 153/255, green: 207/255, blue: 31/255 , alpha: 1.0)
            
        }
    }
    
}
